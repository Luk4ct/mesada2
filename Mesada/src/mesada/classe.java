
package mesada;




public class classe {
    
    String nome,email,cadastro;
    int idade;
    double mesada;
    public static void main(String[] args) {
     
    }

    public classe() {
        this.nome = nome;
        this.email = email;
        this.cadastro = cadastro;
        this.idade = idade;
        this.mesada = mesada;
      
    }
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCadastro() {
        return cadastro;
    }

    public void setCadastro(String cadastro) {
        this.cadastro = cadastro;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public double getMesada() {
        return mesada;
    }

    public void setMesada(double mesada) {
        this.mesada = mesada;
    }

    @Override
    public String toString() {
        return "classe{" + "nome=" + nome + ", email=" + email + ", cadastro=" + cadastro + ", idade=" + idade + ", mesada=" + mesada + '}';
    }

 }

        
    


        
    
            
        
  



     
 

    

 

        

    

    
           
       
      



   
    
    

